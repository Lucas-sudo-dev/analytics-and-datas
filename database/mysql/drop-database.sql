/*
No banco de dados MySQL Server, caso você queira dropar/excluir 
um database ou schema, saiba que ambos são sinônimos ou seja
significam a mesma coisa.
*/

-- 1: COMANDO/SYNTAX
DROP DATABASE NOM_DO_DB;


-- 2: COMANDO/SYNTAX
DROP SCHEMA NOM_DO_SCHEMA;



-- 3: COMANDO/SYNTAX
/*
Comando para dropar o database/schema caso ele exista
*/
DROP DATABASE IF EXISTS NOM_DO_DB;